DROP TABLE IF EXISTS gifts;
CREATE TABLE gifts(
    -- первичный ключ
	id SERIAL PRIMARY KEY,
    -- от кого подарок
	from_user_id BIGINT UNSIGNED NOT NULL,
	-- для кого подарок
    to_user_id BIGINT UNSIGNED NOT NULL,
    -- подпись к подарку
    signature VARCHAR(100),
    -- событие для подарка
    event VARCHAR(40),
    -- дата отправки
    created_at DATETIME DEFAULT NOW(),
    -- внешние ключи
    FOREIGN KEY (from_user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (to_user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE
);


-- 1 - 1
-- у одного пользователя одна статистика страницы
DROP TABLE IF EXISTS page_statistics;
CREATE TABLE page_statistics(
	-- первичный ключ
	id SERIAL PRIMARY KEY,
    -- пользователь
	user_id BIGINT UNSIGNED NOT NULL,
	-- посетителей за день
    visitors_for_today BIGINT UNSIGNED DEFAULT 0,
    -- посетителей за неделю
    visitors_per_week BIGINT UNSIGNED DEFAULT 0,
    -- посетителей женского пола
    female_gender BIGINT UNSIGNED DEFAULT 0,
    -- посетителй мужского пола
    male_gender BIGINT UNSIGNED DEFAULT 0,
    -- города посетителей
    cities VARCHAR(100),
    -- внешний
    FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE
);

-- М - М
-- таблица с играми
DROP TABLE IF EXISTS games;
CREATE TABLE games(
	-- первичный ключ
	id SERIAL PRIMARY KEY,
	-- название игры
	name VARCHAR(150),
	-- разраб игры
	game_developer_id BIGINT UNSIGNED,
	-- индексируем
	INDEX game_name_idx(name),
	-- внешний
	FOREIGN KEY (game_developer_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE SET NULL

);

DROP TABLE IF EXISTS users_games;
CREATE TABLE users_games(
    -- обращение к пользователям
	user_id BIGINT UNSIGNED NOT NULL,
    -- обращение к играм
	game_id BIGINT UNSIGNED NOT NULL,
    -- первичный ключ
	PRIMARY KEY (user_id, game_id),
	-- внешние
    FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (game_id) REFERENCES games(id) ON UPDATE CASCADE ON DELETE CASCADE
);


